    private void addTable(String data) {
        Vector row = null;
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("Mã nhập kho");
        model.addColumn("Tên sản phẩm");
        model.addColumn("Số lượng");
        model.addColumn("Giá nhập");
       
        ChiTietNhapKho ctnk;
        for (int i = 0; i < ctnkd.find(data,"MaNhapKho").size(); i++) {
            ctnk=ctnkd.find(data,"MaNhapKho").get(i);
            row=new Vector();
            row.add(ctnk.getMaNhapKho());
            row.add(ctnkd.getData(ctnk.getMaSanPham(),"MaSP"));
            
            row.add(ctnk.getSoLuong());
            row.add(ctnk.getGiaNhap());
            model.addRow(row);
        }
        table.setModel(model);
    }
