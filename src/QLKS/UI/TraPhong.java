 private void loadThuePhongToJTable() {
        dtmDanhSachThuePhong = new DefaultTableModel();
        String[] columnTitle = {"Mã HD", "ID Đặt phòng", "Mã phòng", "Mã nhân viên", "Mã khách hàng", "Số người", "Đặt cọc", "Ngày đặt phòng", "Ngày nhận phòng", "Thành tiền", "Trạng thái"};
        dtmDanhSachThuePhong.setColumnIdentifiers(columnTitle);
        Object[] dataRow;

        List<HoaDon> listHoaDon = hoaDonDao.getAll();
        for (HoaDon hd : listHoaDon) {
            dataRow = new Object[]{hd.getMaHD(), hd.getID_DatPhong(), hd.getMaPhong(), hd.getMaNV(), hd.getMaKH(), hd.getSoNguoi(), hd.getDatCoc(), hd.getNgayDatPhong(), hd.getNgayNhanPhong(), hd.getThanhTien(), hd.getTrangThai()};
            dtmDanhSachThuePhong.addRow(dataRow);

        }
        tblThuePhong.setModel(dtmDanhSachThuePhong);

    }

    private void loadPhuthuToJTable() {
        dtmDanhSachPhuThu = new DefaultTableModel();
        String[] columnTitle = {"Mã phụ thu", "Mã khách hàng", "Mã sản phẩm", "Số lượng", "Ngày bán", "Thành tiền"};
        dtmDanhSachPhuThu.setColumnIdentifiers(columnTitle);
        Object[] dataRow;

        String MaHD = txtMaHD.getText();

        List<PhuThu> listPhuThu = hoaDonDao.getPhuThuTheoMaHD(MaHD);
        for (PhuThu pt : listPhuThu) {
            dataRow = new Object[]{pt.getMaPT(), pt.getMaKH(), pt.getMaSP(), pt.getSoLuong(), pt.getNgayBan(), pt.getThanhTien()};
            dtmDanhSachPhuThu.addRow(dataRow);

        }
        tblPhuthu.setModel(dtmDanhSachPhuThu);
        PhiPhuThu = hoaDonDao.getPhiPhuThu(txtMaHD.getText());

        txtPhiPhuThu.setText(format.format(PhiPhuThu) + "");

    }

