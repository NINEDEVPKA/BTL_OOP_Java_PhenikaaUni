    private void btnTraPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraPhongActionPerformed

        if (txtSoNgay.getText().trim().equals("")) {
            txtSoNgay.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn phải chọn ngày trả phòng");
            return;
        } else if (txtTongPhi.getText().trim().equals("")) {
            txtTongPhi.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Bạn phải phải chọn ngày để thanh toán");
            return;
        } else {
            Date time2 = (Date) dateNgayTraPhong.getValue();
            Timestamp times2 = new Timestamp(time2.getTime());
            String MaHd = txtMaHD.getText();
            String Phi = txtTongPhi.getText().replaceAll(",", "");
            String maphong = txtMaPhong.getText();
            int ThanhTiens = Integer.parseInt(Phi);
            String ID = txtIDDatPhong.getText();
            int a = 0;

            if (hoaDonDao.updateHoaDon(new HoaDon(MaHd, times2, ThanhTiens)) > 0) {
                if (hoaDonDao.updateNgayTraPhong(new Datphong(ID, times2, a)) > 0) {
                    if (datPhongDao.updateTrangThaiTrong(maphong) > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Trả phòng thành công!");
                        loadThuePhongToJTable();
                        Clear();

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Trả phòng không thành công!");
                    }
                }

            }
        }

    }//GEN-LAST:event_btnTraPhongActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String txtSearch = this.txtSearch.getText();
        String colName = (String) this.cboSearch.getSelectedItem();
        Vector row = null;
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã hóa đơn");
        model.addColumn("ID đặt phòng");
        model.addColumn("Mã phòng");
        model.addColumn("Mã NV");
        model.addColumn("Mã KH");
        model.addColumn("Số người");
        model.addColumn("Đặt cọc");
        model.addColumn("Ngày đặt phòng");
        model.addColumn("Ngày nhận phòng");
        model.addColumn("Thành tiền");
        model.addColumn("Trạng thái");
        {
            HoaDon hd;
            for (int i = 0; i < hoaDonDao.find(txtSearch, colName).size(); i++) {
                hd = hoaDonDao.find(txtSearch, colName).get(i);
                row = new Vector();
                row.add(hd.getMaHD());
                row.add(hd.getID_DatPhong());
                row.add(hd.getMaPhong());
                row.add(hd.getMaNV());
                row.add(hd.getMaKH());
                row.add(hd.getSoNguoi());
                row.add(hd.getDatCoc());
                row.add(hd.getNgayDatPhong());
                row.add(hd.getNgayNhanPhong());
                row.add(hd.getThanhTien());
                row.add(hd.getTrangThai());
                model.addRow(row);
            }
            tblThuePhong.setModel(model);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

