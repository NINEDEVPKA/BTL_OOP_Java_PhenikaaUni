    private void tblThuePhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblThuePhongMouseClicked
        int index = tblThuePhong.getSelectedRow();
        String valueAt = (String) tblThuePhong.getValueAt(index, 0);
        HoaDon pUpdate = hoaDonDao.get(valueAt + "").get();
        txtMaHD.setText(pUpdate.getMaHD());

        txtIDDatPhong.setText(pUpdate.getID_DatPhong());
        txtMaPhong.setText(pUpdate.getMaPhong());
        cboMaKH.getModel().setSelectedItem(pUpdate.getMaKH());
       // cboMaKH.getModel().setSelectedItem(pUpdate.getCMND());
        cboMaNV.getModel().setSelectedItem(pUpdate.getMaNV());

        dateNgayDatPhong.setValue(pUpdate.getNgayDatPhong());
        dateNgayNhanPhong.setValue(pUpdate.getNgayNhanPhong());

        txtSoNg.setText(pUpdate.getSoNguoi() + "");
        txtDatCoc.setText(pUpdate.getDatCoc() + "");

        idUpdaeTraPhong = valueAt;
        String MaPhong = txtMaPhong.getText();

        txtTenPhong.setText(datPhongDao.getTenPhong(MaPhong));
        loadPhuthuToJTable();
        PhiPhong = hoaDonDao.getPhiPhong(txtMaPhong.getText());
        txtPhiPhong.setText(format.format(PhiPhong) + "");
        trangThaiHd = hoaDonDao.getTrangThai(pUpdate.getMaHD());
        txtTongPhi.setText(pUpdate.getThanhTien()+"");

        if (trangThaiHd == 0) {
            btnTraPhong.setEnabled(true);

        } else {
            btnTraPhong.setEnabled(false);
            dateNgayTraPhong.setValue(hoaDonDao.getNgayTraPhong(txtIDDatPhong.getText()));
        }

    }//GEN-LAST:event_tblThuePhongMouseClicked

