     public void addTable()
    {
        Vector row = null;
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("Mã hóa đơn dịch vụ");
        model.addColumn("Tên dịch vụ");
        model.addColumn("Tên khách hàng");
      //  model.addColumn("Tên nhân viên");
        model.addColumn("Thời gian đặt");
        model.addColumn("Thời gian bắt đầu sử dụng dịch vụ");
        model.addColumn("Số lượng dịch vụ");
        model.addColumn("Thành tiền");
        model.addColumn("Ghi chú");
        DatDichVu ddv;
        for (int i = 0; i < ddvd.getList().size(); i++) {
            ddv=ddvd.getList().get(i);
            row=new Vector();
            row.add(ddv.getMaHDDV());
            row.add(ddvd.getData(ddv.getMaDV(),"MaDV"));
            row.add(ddvd.getData(ddv.getMaKH(),"MaKH"));
        //    row.add(ddvd.getData(ddv.getMaNV(),"MaNV"));
            row.add(formatter.format(ddv.getThoiGianDat()));
            row.add(formatter.format(ddv.getThoiGianBatDauSDDV()));
            row.add(ddv.getSoLuong());
            row.add(ddv.getThanhTien());
            row.add(ddv.getGhiChu());
            model.addRow(row);
        }
        table.setModel(model);
        table.getTableHeader().setFont(new Font("Arial", Font.BOLD, 18));
    }
