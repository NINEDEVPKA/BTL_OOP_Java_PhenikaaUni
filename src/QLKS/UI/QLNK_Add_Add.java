    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        LoaiSanPham sp=(LoaiSanPham) cboTenSP.getSelectedItem();
        float ThanhTien=0;
        ctnk.setMaSanPham(sp.getMaSP());
        int SoLuong=(int) spin.getValue();
        try {
            float GiaNhap=Float.parseFloat(txtGiaNhap.getText());
            ctnk.setGiaNhap(GiaNhap);
            ctnk.setSoLuong(SoLuong);
            ctnkd.insert(ctnk);
            ctnkd.updateKho(sp.getMaSP(),SoLuong);
            ThanhTien=(GiaNhap*SoLuong);
            ctnkd.updateNK(ctnk.getMaNhapKho(),ThanhTien);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Nhập đúng định dạng giá nhập !!");
        }
         
        
    }//GEN-LAST:event_btnCreateActionPerformed
    
