    private void addCbo() {
        LoaiSanPham lsp;
        DefaultComboBoxModel model=new DefaultComboBoxModel();
        
        for (int i = 0; i < ctnkd.getListLSP().size(); i++) {
             lsp=ctnkd.getListLSP().get(i);
             model.addElement(lsp);
        }
        cboTenSP.setModel(model);
       
    }

