    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        NhaCungCap ncc=new NhaCungCap();
        NhapKho nk=new NhapKho();
        ncc=(NhaCungCap) cboTenNhaCC.getSelectedItem();
        try {
            
            String MaNK=txtMaNhapKho.getText();
            nk.setMaNhapKho(MaNK);
            nk.setMaNCC(ncc.getMaNCC());
            nk.setChietKhau((int)spin.getValue());
            nk.setThanhTien(0);
            nk.setNgayNhap(timestamp);
            nk.setMaNV(new PropertiesNVNow().getRemember().get(0)); 
            
         if(!MaNK.trim().equals("") && MaNK!=null)
         {
             if(nkd.findT(MaNK)==0)
             {
             nkd.insert(nk);
             QLNK_Add_Add add_add=new QLNK_Add_Add(MaNK);
             add_add.setVisible(true);
             this.setVisible(false);
             }
             else
             {
                 JOptionPane.showMessageDialog(null,"Thêm thất bại ( Trùng mã nhập kho) !!");
             }
             
             
         }
         else
         {
             JOptionPane.showMessageDialog(null,"Điền đầy đủ thông tin mã nhập kho !!");
         }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,"Phải nhập đúng kiểu dữ liệu !");
        }
        
        
        
        
    }//GEN-LAST:event_btnCreateActionPerformed

