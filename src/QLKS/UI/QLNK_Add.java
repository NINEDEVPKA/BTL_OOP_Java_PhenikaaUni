    private void addCbo() {
        NhaCungCap ncc;
        DefaultComboBoxModel model=new DefaultComboBoxModel();
        for (int i = 0; i < nkd.getListNCC().size(); i++) {
             ncc=nkd.getListNCC().get(i);
             model.addElement(ncc);
        }
        cboTenNhaCC.setModel(model);
       
    }
